// Get translation for a specific key
function getTranslation(language, module, key) {
  const lang = translations[language] || translations.en;
  const mod = lang[module];

  if (!mod) return key; // Fallback to key name

  return mod[key] || key; // Fallback to key name if not found
}

// Get all translations for a module
function getModuleTranslations(language, module) {
  const lang = translations[language] || translations.en;
  return lang[module] || {};
}

// List available languages
function getAvailableLanguages() {
  return Object.keys(translations).map((code) => ({
    code,
    name:
      {
        en: 'English',
        hi: 'हिन्दी',
        ta: 'தமிழ்',
        te: 'తెలుగు',
      }[code] || code,
  }));
}

